export class Student {
    idStudent: string;
    nameStudent: string;
    classStudent: string;
    addressStudent: string;
    scoreStudent: number;
    interset: string;

    constructor(idStudent: string, nameStudent: string, classStudent: string, addressStudent: string, scoreStudent: number, interset: string) {
        this.idStudent = idStudent;
        this.nameStudent = nameStudent;
        this.classStudent = classStudent;
        this.addressStudent = addressStudent;
        this.scoreStudent = scoreStudent;
        this.interset = interset;
    }
}